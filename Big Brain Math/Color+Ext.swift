import SwiftUI

extension Color {
    static let brandPrimary     = Color("brandPrimary")
    static let darkPrimary      = Color("darkPrimary")
    static let lightPrimary     = Color("lightPrimary")
    static let brandSecondary   = Color("brandSecondary")
}

extension UIColor {
    static let brandPrimary     = UIColor(named: "brandPrimary")
    static let darkPrimary      = UIColor(named: "darkPrimary")
    static let lightPrimary     = UIColor(named: "lightPrimary")
    static let brandSecondary   = UIColor(named: "brandSecondary")
}
